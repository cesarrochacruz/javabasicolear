package com.calculadora;

public class Division {

	public Division() {
		
	}
	
	public int operacion(int a,int b) {
		int resultado = 0;
		resultado = a / b;
		return resultado;
	}
	public double operacion(double a, double b) {
		double resultado = 0;
		resultado = a / b;
		return resultado;
	}
}
