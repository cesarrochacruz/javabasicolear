package com.calculadora;

public class Multipli {
	public Multipli() {
		
	}
	
	public int operacion(int a,int b) {
			int resultado = 0;
			resultado = a * b;
			return resultado;
		
	}
	
	public int operacion(int a,int b,int c) {
			int resultado = 0;
			resultado = (a * b) * c;
			return resultado;
		
	}
}
